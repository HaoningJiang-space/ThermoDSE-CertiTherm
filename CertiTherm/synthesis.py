"""Exact zero-error decision observation synthesis by constraint generation.

The finite master is a minimum-cost hitting-set MILP.  Its constraints are
generated by a continuous LP oracle that searches for two admissible power
worlds with different thermal decisions but identical selected observations.
When the oracle finds no collision, the master lower bound and feasible plan
meet: the returned batch plan is globally minimum-cost for the registered
action library, model family, margins, and numerical tolerances.
"""

